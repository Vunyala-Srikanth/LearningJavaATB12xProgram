package ex_10_For_Loop;

public class Lab103_For_If_LOOP {
    public static void main(String[] args) {
        for (int sri =0;sri <18 ;sri++){  // 0 to 17, 18 times
            if(sri>16){
                System.out.println("He is eligible for ssc exam");
            }
            else {
                System.out.println("Not elegible for ssc");
            }
        }


    }

}
