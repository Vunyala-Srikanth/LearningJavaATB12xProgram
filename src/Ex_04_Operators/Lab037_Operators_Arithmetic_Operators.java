package Ex_04_Operators;

public class Lab037_Operators_Arithmetic_Operators {
    public static void main(String[] args) {
        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder
        int a=20,b=15;
        float c=3.14f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a-c);
        System.out.println(a/c);
        System.out.println(a*c);

    }
}
