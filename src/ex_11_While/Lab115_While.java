package ex_11_While;

public class Lab115_While {
    public static void main(String[] args) {
        int i =0;
        while(i<10){// Condition (must be true to enter the loop)
            System.out.println(i);
            i++;//updation
        }
        System.out.println("For loop start");
        //  I, C, U
        for(int j =0;j<10;j++){
            System.out.println(j);


        }
    }
}
